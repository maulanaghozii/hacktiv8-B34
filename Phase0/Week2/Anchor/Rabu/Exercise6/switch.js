// Melakukan Looping menggunakan Switch
var number = 2;
console.log('LOOPING PERTAMA');
while(number <= 20){
    console.log(number + ' - I Love Coding');
    number = number + 2;
};

var numberA = 20;
console.log('LOOPING KEDUA');
while(numberA >= 2){
    console.log(numberA + ' - I will become fullstack developer');
    numberA = numberA - 2;
}